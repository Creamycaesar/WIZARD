/// <summary>
/// The eight schools of magic from 5e.
/// Used by SpellData to categorize spells and by future Wizard subclass
/// features that key off specific schools (e.g. Evocation Wizard).
/// </summary>
public enum SpellSchool
{
    Abjuration,
    Conjuration,
    Divination,
    Enchantment,
    Evocation,
    Illusion,
    Necromancy,
    Transmutation
}